def build_raw_video_catalog_df(links_df):
    if links_df.empty:
        return pd.DataFrame(
            columns=[
                "institution",
                "platform",
                "video_title",
                "video_subject",
                "video_published_at",
                "country",
                "continent",
                "video_link",
            ]
        )

    catalog = links_df.copy().reset_index(drop=True)
    catalog["_source_index"] = catalog.index
    for column in ["video_title", "video_subject", "video_description", "video_published_at"]:
        if column not in catalog.columns:
            catalog[column] = ""

    catalog["video_title"] = catalog["video_title"].fillna("").astype(str).str.strip()
    catalog["video_subject"] = catalog["video_subject"].fillna("").astype(str).str.strip()
    catalog["video_published_at"] = catalog["video_published_at"].fillna("").astype(str).str.strip()
    catalog["video_title_display"] = catalog["video_title"]
    catalog.loc[catalog["video_title_display"] == "", "video_title_display"] = (
        catalog.loc[catalog["video_title_display"] == "", "video_subject"]
    )
    catalog.loc[catalog["video_title_display"] == "", "video_title_display"] = (
        catalog.loc[catalog["video_title_display"] == "", "video_link"]
    )
    catalog["video_date_display"] = catalog["video_published_at"].apply(format_video_date)
    catalog["video_theme"] = catalog.apply(infer_video_theme, axis=1)
    catalog["access_surface"] = catalog.apply(classify_access_surface, axis=1)

    return catalog.sort_values(
        ["institution", "platform", "video_date_display", "video_title_display"],
        ascending=[True, True, False, True],
    )


def filter_curatorial_video_catalog(video_catalog_df):
    if video_catalog_df.empty or "video_theme" not in video_catalog_df.columns:
        return video_catalog_df.copy()
    return video_catalog_df.loc[video_catalog_df["video_theme"] != NOISE_THEME_LABEL].copy()


def build_video_catalog_df(links_df):
    catalog = build_raw_video_catalog_df(links_df)
    filtered_catalog = filter_curatorial_video_catalog(catalog)
    return filtered_catalog.drop(columns=["_source_index"], errors="ignore")


def filter_in_scope_video_links_df(links_df):
    if links_df is None:
        return pd.DataFrame()
    if links_df.empty:
        return links_df.copy()
    catalog = build_raw_video_catalog_df(links_df)
    filtered_catalog = filter_curatorial_video_catalog(catalog)
    if filtered_catalog.empty or "_source_index" not in filtered_catalog.columns:
        return links_df.iloc[0:0].copy()
    source_indices = filtered_catalog["_source_index"].astype(int).tolist()
    return links_df.iloc[source_indices].reset_index(drop=True).copy()


