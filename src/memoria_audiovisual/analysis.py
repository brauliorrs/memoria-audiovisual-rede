def build_institution_access_fields(summary_df, video_catalog_df=None):
    if summary_df.empty:
        return pd.DataFrame(columns=["slug", "access_modalities_detected", "access_regime"])

    if video_catalog_df is None:
        modality_map = {}
    else:
        filtered_catalog = filter_curatorial_video_catalog(video_catalog_df)
        if filtered_catalog.empty or "slug" not in filtered_catalog.columns or "access_surface" not in filtered_catalog.columns:
            modality_map = {}
        else:
            modality_map = (
                filtered_catalog.groupby("slug")["access_surface"]
                .apply(lambda values: sorted(dict.fromkeys([normalize_optional_text(value) for value in values if normalize_optional_text(value)])))
                .to_dict()
            )

    rows = []
    for _, row in summary_df.iterrows():
        slug = normalize_optional_text(row.get("slug"))
        modalities = modality_map.get(slug, [])
        rows.append(
            {
                "slug": slug,
                "access_modalities_detected": ", ".join(modalities),
                "access_regime": classify_access_regime(modalities, row.get("audiovisual_visibility", "")),
            }
        )
    return pd.DataFrame(rows)


def build_detected_sites_df(summary_df, links_df):
    if summary_df.empty:
        return summary_df.copy()

    ranked = summary_df.copy()
    ranked["video_links_found_total"] = pd.to_numeric(
        ranked.get("video_links_found_total", 0),
        errors="coerce",
    ).fillna(0)
    ranked["embedded_video_signals_total"] = pd.to_numeric(
        ranked.get("embedded_video_signals_total", 0),
        errors="coerce",
    ).fillna(0)
    ranked = ranked.loc[ranked["video_links_found_total"] > 0].copy()
    if ranked.empty:
        return ranked

    platforms_df = pd.DataFrame(columns=["slug", "platforms_detectadas"])
    if not links_df.empty:
        platforms_df = (
            links_df.groupby("slug")["platform"]
            .apply(lambda values: ", ".join(sorted(pd.unique(values))))
            .reset_index(name="platforms_detectadas")
        )

    ranked = ranked.merge(platforms_df, on="slug", how="left")
    ranked["platforms_detectadas"] = ranked["platforms_detectadas"].fillna("")
    return ranked.sort_values(
        [
            "video_links_found_total",
            "embedded_video_signals_total",
            "integrity_status",
            "institution",
        ],
        ascending=[False, False, True, True],
    )


