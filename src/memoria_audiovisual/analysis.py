def build_theme_country_summary(video_catalog_df, top_n=10):
    filtered_catalog = filter_curatorial_video_catalog(video_catalog_df)
    if filtered_catalog.empty:
        return pd.DataFrame(columns=["tema", "pais", "total"])
    summary = (
        filtered_catalog.assign(
            country=filtered_catalog["country"].fillna(UNCLASSIFIED_LABEL).replace("", UNCLASSIFIED_LABEL)
        )
        .groupby(["video_theme", "country"])
        .size()
        .reset_index(name="total")
        .sort_values(["total", "video_theme", "country"], ascending=[False, True, True])
        .rename(columns={"video_theme": "tema", "country": "pais"})
    )
    return _limit_summary(summary, top_n)


def build_theme_country_matrix(video_catalog_df, top_countries=10, top_themes=8):
    filtered_catalog = filter_curatorial_video_catalog(video_catalog_df)
    if filtered_catalog.empty:
        return pd.DataFrame()

    filtered_catalog["country_display"] = (
        filtered_catalog["country"].fillna(UNCLASSIFIED_LABEL).replace("", UNCLASSIFIED_LABEL)
    )
    top_country_values = filtered_catalog["country_display"].value_counts().head(top_countries).index.tolist()
    top_theme_values = filtered_catalog["video_theme"].value_counts().head(top_themes).index.tolist()

    matrix_source = filtered_catalog[
        filtered_catalog["country_display"].isin(top_country_values)
        & filtered_catalog["video_theme"].isin(top_theme_values)
    ].copy()
    if matrix_source.empty:
        return pd.DataFrame()

    matrix = pd.crosstab(matrix_source["country_display"], matrix_source["video_theme"])
    matrix = matrix.reindex(index=top_country_values, columns=top_theme_values, fill_value=0)
    matrix.index.name = "país"
    return matrix.reset_index()


