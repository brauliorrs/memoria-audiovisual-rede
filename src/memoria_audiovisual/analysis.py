def classify_audiovisual_visibility(row):
    status = normalize_optional_text(row.get("integrity_status"))
    video_links = pd.to_numeric(row.get("video_links_found_total"), errors="coerce")
    embedded_signals = pd.to_numeric(row.get("embedded_video_signals_total"), errors="coerce")
    internal_pages = pd.to_numeric(row.get("candidate_internal_pages"), errors="coerce")

    video_links = 0 if pd.isna(video_links) else int(video_links)
    embedded_signals = 0 if pd.isna(embedded_signals) else int(embedded_signals)
    internal_pages = 0 if pd.isna(internal_pages) else int(internal_pages)

    if video_links > 0:
        return "Evidência pública detectável de audiovisual"
    if embedded_signals > 0:
        return "Evidência pública indireta de audiovisual"
    if status in {"integro", "acessivel"} and internal_pages > 0:
        return "Potencial audiovisual em navegação interna"
    if status in {"integro", "acessivel"}:
        return "Sem evidência pública detectável de audiovisual"
    return "Site indisponível para verificação"


