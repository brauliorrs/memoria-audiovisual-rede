def build_video_catalog_df(links_df):
    if links_df.empty:
        return pd.DataFrame(
            columns=[
                "institution",
                "platform",
                "video_title",
                "video_subject",
                "video_published_at",
                "country",
                "continent",
                "video_link",
            ]
        )

    catalog = links_df.copy()
    for column in ["video_title", "video_subject", "video_description", "video_published_at"]:
        if column not in catalog.columns:
            catalog[column] = ""

    catalog["video_title"] = catalog["video_title"].fillna("").astype(str).str.strip()
    catalog["video_subject"] = catalog["video_subject"].fillna("").astype(str).str.strip()
    catalog["video_published_at"] = catalog["video_published_at"].fillna("").astype(str).str.strip()
    catalog["video_title_display"] = catalog["video_title"]
    catalog.loc[catalog["video_title_display"] == "", "video_title_display"] = (
        catalog.loc[catalog["video_title_display"] == "", "video_subject"]
    )
    catalog.loc[catalog["video_title_display"] == "", "video_title_display"] = (
        catalog.loc[catalog["video_title_display"] == "", "video_link"]
    )
    catalog["video_date_display"] = catalog["video_published_at"].apply(format_video_date)
    catalog["video_theme"] = catalog.apply(infer_video_theme, axis=1)

    return catalog.sort_values(
        ["institution", "platform", "video_date_display", "video_title_display"],
        ascending=[True, True, False, True],
    )


