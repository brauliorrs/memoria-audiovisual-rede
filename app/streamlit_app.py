def build_theme_country_matrix(video_catalog_df, top_countries=10, top_themes=8):
    if video_catalog_df.empty or "video_theme" not in video_catalog_df.columns:
        return pd.DataFrame()
    filtered_catalog = video_catalog_df.loc[
        video_catalog_df["video_theme"] != "Ruído de plataforma ou incorporação externa"
    ].copy()
    if filtered_catalog.empty:
        return pd.DataFrame()

    filtered_catalog["country_display"] = (
        filtered_catalog["country"]
        .fillna("Não classificado")
        .replace("", "Não classificado")
    )
    top_country_values = (
        filtered_catalog["country_display"].value_counts().head(top_countries).index.tolist()
    )
    top_theme_values = (
        filtered_catalog["video_theme"].value_counts().head(top_themes).index.tolist()
    )

    matrix_source = filtered_catalog[
        filtered_catalog["country_display"].isin(top_country_values)
        & filtered_catalog["video_theme"].isin(top_theme_values)
    ].copy()
    if matrix_source.empty:
        return pd.DataFrame()

    matrix = pd.crosstab(
        matrix_source["country_display"],
        matrix_source["video_theme"],
    )
    matrix = matrix.reindex(index=top_country_values, columns=top_theme_values, fill_value=0)
    matrix.index.name = "país"
    return matrix.reset_index()


