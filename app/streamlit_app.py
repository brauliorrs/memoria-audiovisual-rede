def classify_availability_group(integrity_status):
    status = normalize_optional_text(integrity_status)
    if status in {"integro", "acessivel"}:
        return "Disponível"
    return "Indisponível"


def classify_availability_reason(row):
    status = normalize_optional_text(row.get("integrity_status"))
    if status == "integro":
        return "Disponível: íntegro"
    if status == "acessivel":
        return "Disponível: acessível"
    if status == "suspeito":
        return "Indisponível: quebrado (página suspeita)"
    if status == "restrito":
        return "Indisponível: restrito"
    if status == "quebrado":
        return "Indisponível: quebrado (erro explícito)"
    if status == "instavel":
        error_text = normalize_optional_text(row.get("error")).lower()
        if "timeout" in error_text:
            return "Indisponível: instável (timeout ou falha de carregamento)"
        if error_text:
            return "Indisponível: instável (erro de navegação)"
        return "Indisponível: instável"
    if status == "sem_site":
        return "Indisponível: sem site"
    return "Indisponível: outro"


